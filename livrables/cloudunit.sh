#!/bin/bash

#############################################
#############################################
##               CONFIGURATION
#############################################
#############################################

CU_INSTALL_LOCAL_DIR="$HOME/cloudunit"
CU_JAR_NAME=CloudUnitCLI.jar

#############################################
#############################################
##               FUNCTIONS
#############################################
#############################################

run()
{
    # Run the program with public certificat without you cannot connect
    java -Djavax.net.ssl.trustStore=cloudunit-cert.jks -jar $CU_JAR_NAME
}

#############################################
#############################################
##               MAIN BLOCK
#############################################
#############################################

run



